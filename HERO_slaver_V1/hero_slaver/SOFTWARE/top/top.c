float my_cos(int angel)
{
	float value = 0;
	
	if(angel <= 0)
	{
		angel = -angel;
	}
	
	angel = angel%360;

	if(angel>180)
	{
		angel = 360-angel;
	}
	
	if(angel<=90)
	{
		value =	 DCOS_TABLE[angel];
	}
	else if( 90<angel && angel<=180)
	{
		value = -DCOS_TABLE[180-angel];
	}
	
	value = value/4192.0f;
	
	return value;
}

float my_sin(int angel)
{
	float value;
	
	angel = angel-90;
	
	value = my_cos(angel);
	
	return value;
}

